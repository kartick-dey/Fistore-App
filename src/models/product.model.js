class Product {
    constructor(id, userId, fishName, fishType, price, unit, image, location, contact, createdAt, updatedAt) {
        this.id = id;
        this.userId = userId;
        this.fishName = fishName;
        this.fishType = fishType;
        this.price = price;
        this.unit = unit;
        this.image = image;
        this.location = location;
        this.contact = contact;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt
    }
}